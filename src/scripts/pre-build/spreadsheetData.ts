const getDocId = (url = '') => {
    const idIndex = url.indexOf('id=');
    if (~idIndex) {
        return url.slice(idIndex + 3);
    } else {
        return url.split('/')[5] || url;
    }
};

function normalazePageNumb(page = '') {
    let additional = '';
    switch (page.length) {
        case 1:
            additional = '000';
            break;
        case 2:
            additional = '00';
            break;
        case 3:
            additional = '0';
            break;
        default:
            additional = '';
    }
    return `${additional}${page}`;
}

const handleNumber = (num = '') => {
    return isNaN(+num) ? num.length ? num : '' : +num;
}


